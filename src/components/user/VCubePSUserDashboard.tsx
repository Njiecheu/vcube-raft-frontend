      setError('');
      setSuccess('');
      
      // Pour les siÃ¨ges gÃ©nÃ©rÃ©s cÃ´tÃ© client, utiliser un ID simplifiÃ©
      let actualSeatId = selectedSeat;
      if (selectedSeat.startsWith('seat-')) {
        // Convertir en format numÃ©rique simple
        const seatNumber = selectedSeatNumber || 1;
        actualSeatId = seatNumber.toString();
      }
      
      const reservationData = {
        userId,
        providerId: currentProviderId,
        vehicleId: selectedVehicleId,
        seatId: actualSeatId
      };
      
      console.log('DonnÃ©es de rÃ©servation:', reservationData);
      
      // CrÃ©er la rÃ©servation
      const result = await apiService.createReservation(reservationData);
      console.log('RÃ©servation crÃ©Ã©e:', result);

